#include <iostream>

int main()
{
    //Declarar variables (inicializadas)
    int i = 1915;
    bool pepe = true;
    float p = 1.440, q = 2.71;
    double a = 1.0e30, b = 2.71e-43;
    char d = 14;

    //Imprimir variables (inicializadas)
    std::cout << "i = " << i << "\n";
    std::cout << "pepe = " << pepe << "\n";
    std::cout << "p = " << p << "\n";
    std::cout << "q = " << q << "\n";
    std::cout << "a = " << a << "\n";
    std::cout << "d = " << d << "\n";
    return 0;

}